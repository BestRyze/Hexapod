int main(void)
{
  
	SCB->VTOR = 0x90000000; /* 设置中断向量表地址 */
  sys_cache_enable();
  //MPU_Config();   //去掉注释会导致进入异常

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();
  /* Configure the system clock */
  SystemClock_Config();
	MX_DMA_Init();
  MX_GPIO_Init();
	/************硬件初始化***************/
	Debug_UART_Init();
	Remote_Init();
	DWT_Delay_Init();
	/************启用FreeRTOS***************/
	MX_FREERTOS_Init();  //初始化任务
	osKernelStart(); //开始任务调度
	//开始任务调度后这里不会被执行
  while (1)
  {
		HAL_GPIO_TogglePin(LED_GPIO_Port,LED_Pin);
		//APP_PRINT("HelloWorld\r\n");
		HAL_Delay(1);
  }
}
